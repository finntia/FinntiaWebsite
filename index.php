		$("#contact_submit").submit(function(){
			var data = new FormData(this);
			$.ajax({
				type: "POST",
				url: "contact.php",
				data: data,
				cache:false,
				contentType: false,
				processData: false,
				success: function(data) {
					if(data == 'sent'){
						$("#line").val(0);
						$("#fullname").val("");
						$("#email").val("");
						$("#message").val("");
						
						$(".success").css("display","block");
						$(".error").css("display","none");
						$(".validation").css("display","none");
						$(".recaptcha_validation").css("display","none");
						$(".recaptcha_error").css("display","none");
					}else if(data == 'validation'){
						$(".success").css("display","none");
						$(".error").css("display","none");
						$(".validation").css("display","block");
						$(".recaptcha_validation").css("display","none");
						$(".recaptcha_error").css("display","none");
					}else if(data == 'recaptcha_error'){
						$(".success").css("display","none");
						$(".error").css("display","none");
						$(".validation").css("display","none");
						$(".recaptcha_validation").css("display","none");
						$(".recaptcha_error").css("display","block");
					}else if(data == 'recaptcha_validation'){
						$(".success").css("display","none");
						$(".error").css("display","none");
						$(".validation").css("display","none");
						$(".recaptcha_validation").css("display","block");
						$(".recaptcha_error").css("display","none");
					}else{
						$(".success").css("display","none");
						$(".error").css("display","block");
						$(".validation").css("display","none");
						$(".recaptcha_validation").css("display","none");
						$(".recaptcha_error").css("display","none");
					}
				}
			});
			return false;
		});
	});

